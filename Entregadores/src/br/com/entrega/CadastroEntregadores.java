    public void inserir() {
        String sql = "insert into cadastrologin( nomeentregador,cpf, nomecompleto, datanacimento,cidade,enderecoo,telefone) values( ?, ?, ?, ?, ?, ?,?)";
        
        try {
            pst = conexao.prepareStatement(sql);

            
            
            pst.setString(1, txnome.getText());
            pst.setString(2, txcpf.getText());
            pst.setString(3, txemail.getText());
            pst.setString(4, txdatanacimento.getText());
            pst.setString(5, txcidade.getText());
            pst.setString(6, txenderecop.getText());
            pst.setString(7, txtelefone.getText());
            

            pst.executeUpdate();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    
    }

